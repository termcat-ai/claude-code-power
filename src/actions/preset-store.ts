import * as fs from 'fs';
import * as os from 'os';
import * as path from 'path';
import chokidar, { type FSWatcher } from 'chokidar';
import { emptyPresetsFile, migratePreset, type Preset, type PresetsFile } from './preset-types';

const DATA_DIR = path.join(os.homedir(), '.termcat', 'plugins', 'claude_code_power');
const PRESETS_PATH = path.join(DATA_DIR, 'presets.json');
const ACTIVE_ENV_PATH = path.join(DATA_DIR, 'active.env');
const ACTIVE_PS1_PATH = path.join(DATA_DIR, 'active.ps1');
const IS_WIN = process.platform === 'win32';

/** Shell-escape: wrap in single quotes, internal quotes become `'\''`. */
export function shellEscape(value: string): string {
  return "'" + value.replaceAll("'", "'\\''") + "'";
}

/** PowerShell single-quote escape: wrap in single quotes, internal `'` becomes `''`. */
export function psEscape(value: string): string {
  return "'" + value.replaceAll("'", "''") + "'";
}

/**
 * Map a Preset to the active.env file body.
 *
 * The file starts with a cleanup prologue that unsets any vars written by the
 * previous `source` (tracked via `__TC_CCP_MANAGED_KEYS`). Without this, a
 * preset that omits a field would inherit the previous preset's value —
 * e.g. picking a bare preset after an Anthropic one would still see the old
 * ANTHROPIC_API_KEY in the shell.
 */
export function generateActiveEnv(preset: Preset): string {
  const header = [
    '# Generated by TermCat claude-code-power plugin - do not edit',
    `# Active preset: ${preset.name} (${preset.id})`,
    `# Generated at: ${new Date().toISOString()}`,
    '',
  ];

  const keysToSet: string[] = [];
  const exportLines: string[] = [];

  const push = (key: string, line: string) => {
    keysToSet.push(key);
    exportLines.push(line);
  };

  if (preset.apiKey) push('ANTHROPIC_API_KEY', `export ANTHROPIC_API_KEY=${shellEscape(preset.apiKey)}`);
  if (preset.authToken) push('ANTHROPIC_AUTH_TOKEN', `export ANTHROPIC_AUTH_TOKEN=${shellEscape(preset.authToken)}`);
  if (preset.baseUrl) push('ANTHROPIC_BASE_URL', `export ANTHROPIC_BASE_URL=${shellEscape(preset.baseUrl)}`);
  if (preset.model) push('ANTHROPIC_MODEL', `export ANTHROPIC_MODEL=${shellEscape(preset.model)}`);
  if (typeof preset.maxTokens === 'number') {
    push('ANTHROPIC_MAX_TOKENS', `export ANTHROPIC_MAX_TOKENS=${preset.maxTokens}`);
  }
  for (const [k, v] of Object.entries(preset.extraEnv ?? {})) {
    if (!/^[A-Z_][A-Z0-9_]*$/.test(k)) continue;
    push(k, `export ${k}=${shellEscape(v)}`);
  }

  const cleanup = [
    '# Cleanup: drop vars managed by a previous source of this file.',
    '# `eval "set -- $var"` is used instead of `for k in $var` because zsh',
    '# does not word-split unquoted parameter expansions by default — bare',
    '# iteration would pass the entire string as a single name to `unset`.',
    'if [ -n "${__TC_CCP_MANAGED_KEYS:-}" ]; then',
    '  eval "set -- $__TC_CCP_MANAGED_KEYS"',
    '  for __tc_ccp_k in "$@"; do unset "$__tc_ccp_k"; done',
    '  unset __tc_ccp_k',
    'fi',
    '',
  ];

  // Record what this source set so the NEXT source can clean up.
  const marker = keysToSet.length
    ? `export __TC_CCP_MANAGED_KEYS=${shellEscape(keysToSet.join(' '))}`
    : 'unset __TC_CCP_MANAGED_KEYS';

  return [...header, ...cleanup, ...exportLines, marker, ''].join('\n');
}

/**
 * PowerShell counterpart of `generateActiveEnv`. Emits `$env:KEY = '...'` lines
 * and uses `$env:__TC_CCP_MANAGED_KEYS` (space-separated) for cleanup, mirroring
 * the POSIX file's contract so the two paths are interchangeable.
 */
export function generateActivePs1(preset: Preset): string {
  const header = [
    '# Generated by TermCat claude-code-power plugin - do not edit',
    `# Active preset: ${preset.name} (${preset.id})`,
    `# Generated at: ${new Date().toISOString()}`,
    '',
  ];

  const keysToSet: string[] = [];
  const setLines: string[] = [];

  const push = (key: string, line: string) => {
    keysToSet.push(key);
    setLines.push(line);
  };

  if (preset.apiKey) push('ANTHROPIC_API_KEY', `$env:ANTHROPIC_API_KEY = ${psEscape(preset.apiKey)}`);
  if (preset.authToken) push('ANTHROPIC_AUTH_TOKEN', `$env:ANTHROPIC_AUTH_TOKEN = ${psEscape(preset.authToken)}`);
  if (preset.baseUrl) push('ANTHROPIC_BASE_URL', `$env:ANTHROPIC_BASE_URL = ${psEscape(preset.baseUrl)}`);
  if (preset.model) push('ANTHROPIC_MODEL', `$env:ANTHROPIC_MODEL = ${psEscape(preset.model)}`);
  if (typeof preset.maxTokens === 'number') {
    push('ANTHROPIC_MAX_TOKENS', `$env:ANTHROPIC_MAX_TOKENS = '${preset.maxTokens}'`);
  }
  for (const [k, v] of Object.entries(preset.extraEnv ?? {})) {
    if (!/^[A-Z_][A-Z0-9_]*$/.test(k)) continue;
    push(k, `$env:${k} = ${psEscape(v)}`);
  }

  const cleanup = [
    '# Cleanup: drop vars managed by a previous dot-source of this file.',
    'if ($env:__TC_CCP_MANAGED_KEYS) {',
    "  foreach ($__tc_ccp_k in $env:__TC_CCP_MANAGED_KEYS.Split(' ')) {",
    '    if ($__tc_ccp_k) { Remove-Item -LiteralPath "Env:$__tc_ccp_k" -ErrorAction SilentlyContinue }',
    '  }',
    '  Remove-Variable __tc_ccp_k -ErrorAction SilentlyContinue',
    '}',
    '',
  ];

  const marker = keysToSet.length
    ? `$env:__TC_CCP_MANAGED_KEYS = ${psEscape(keysToSet.join(' '))}`
    : "Remove-Item -LiteralPath 'Env:__TC_CCP_MANAGED_KEYS' -ErrorAction SilentlyContinue";

  return [...header, ...cleanup, ...setLines, marker, ''].join('\n');
}

export class PresetStore {
  private cache: PresetsFile = emptyPresetsFile();
  private writeChain: Promise<void> = Promise.resolve();
  private watcher: FSWatcher | null = null;
  private externalChangeListeners: Array<() => void> = [];
  private lastWrittenMtime = 0;

  async load(): Promise<PresetsFile> {
    await fs.promises.mkdir(DATA_DIR, { recursive: true, mode: 0o700 }).catch(() => {});
    try {
      const text = await fs.promises.readFile(PRESETS_PATH, 'utf-8');
      const parsed = JSON.parse(text);
      if (parsed && typeof parsed === 'object' && Array.isArray(parsed.presets)) {
        this.cache = {
          version: 1,
          activePresetId: parsed.activePresetId ?? null,
          // Migrate each preset to the new split-fields shape.
          presets: (parsed.presets as unknown[]).map(migratePreset),
        };
      }
    } catch {
      // corrupt or missing — preserve broken copy if it exists
      try {
        await fs.promises.access(PRESETS_PATH);
        await fs.promises.rename(PRESETS_PATH, `${PRESETS_PATH}.corrupt-${Date.now()}.json`);
      } catch {
        /* no file — fine */
      }
      this.cache = emptyPresetsFile();
    }
    return this.cache;
  }

  list(): Preset[] {
    return this.cache.presets.slice();
  }

  get(id: string): Preset | null {
    return this.cache.presets.find((p) => p.id === id) ?? null;
  }

  getActive(): Preset | null {
    if (!this.cache.activePresetId) return null;
    return this.get(this.cache.activePresetId);
  }

  async upsert(preset: Preset): Promise<void> {
    const idx = this.cache.presets.findIndex((p) => p.id === preset.id);
    if (idx >= 0) this.cache.presets[idx] = preset;
    else this.cache.presets.push(preset);
    await this.persist();
  }

  async delete(id: string): Promise<void> {
    this.cache.presets = this.cache.presets.filter((p) => p.id !== id);
    if (this.cache.activePresetId === id) this.cache.activePresetId = null;
    await this.persist();
  }

  async setActive(id: string | null): Promise<void> {
    this.cache.activePresetId = id;
    await this.persist();
  }

  async writeActiveEnv(preset: Preset): Promise<void> {
    await fs.promises.mkdir(DATA_DIR, { recursive: true, mode: 0o700 }).catch(() => {});
    const target = IS_WIN ? ACTIVE_PS1_PATH : ACTIVE_ENV_PATH;
    const body = IS_WIN ? generateActivePs1(preset) : generateActiveEnv(preset);
    const tmp = target + '.tc-tmp';
    await fs.promises.writeFile(tmp, body, { encoding: 'utf-8', mode: 0o600 });
    await fs.promises.rename(tmp, target);
    try {
      await fs.promises.chmod(target, 0o600);
    } catch {
      /* best-effort */
    }
  }

  activeEnvPath(): string {
    return IS_WIN ? ACTIVE_PS1_PATH : ACTIVE_ENV_PATH;
  }

  onDidChange(cb: () => void): { dispose: () => void } {
    this.externalChangeListeners.push(cb);
    if (!this.watcher) {
      this.watcher = chokidar.watch(PRESETS_PATH, { persistent: true, ignoreInitial: true });
      this.watcher.on('change', async () => {
        try {
          const stat = await fs.promises.stat(PRESETS_PATH);
          if (Math.abs(stat.mtimeMs - this.lastWrittenMtime) < 5) return; // our echo
        } catch {
          /* ignore */
        }
        await this.load();
        for (const l of this.externalChangeListeners) l();
      });
    }
    return {
      dispose: () => {
        this.externalChangeListeners = this.externalChangeListeners.filter((l) => l !== cb);
        if (this.externalChangeListeners.length === 0 && this.watcher) {
          this.watcher.close().catch(() => {});
          this.watcher = null;
        }
      },
    };
  }

  private persist(): Promise<void> {
    // Chain writes so concurrent mutations don't race.
    this.writeChain = this.writeChain.then(() => this.doPersist());
    return this.writeChain;
  }

  private async doPersist(): Promise<void> {
    await fs.promises.mkdir(DATA_DIR, { recursive: true, mode: 0o700 }).catch(() => {});
    const tmp = PRESETS_PATH + '.tc-tmp';
    const payload = JSON.stringify(this.cache, null, 2) + '\n';
    await fs.promises.writeFile(tmp, payload, { encoding: 'utf-8', mode: 0o600 });
    await fs.promises.rename(tmp, PRESETS_PATH);
    try {
      await fs.promises.chmod(PRESETS_PATH, 0o600);
      const stat = await fs.promises.stat(PRESETS_PATH);
      this.lastWrittenMtime = stat.mtimeMs;
    } catch {
      /* best-effort */
    }
  }
}
